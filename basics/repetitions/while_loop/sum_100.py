# Display start message
print("Calculating the sum of the first 100 numbers...")
# Declare a control variable
number = 1
# Calculate sum of first 100 numbers
sum = 0
# while loop
while (number <= 100):
    sum = sum + number
    number = number + 1
# Display result
print(f"...Done! The answer is {sum}")
